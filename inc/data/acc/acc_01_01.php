<h2 class="TextSubHeading1">Future Developments</h2>
<p class="TextBodyCopy">The Group has made significant investment across the business over the past few years as it prepares to take advantage of the opportunities the post quota environment will bring. The Group will continue to invest in its people, develop new products and build and enhance routes to market ensuring its role in driving the growth potential of the Irish dairy sector.</p>
