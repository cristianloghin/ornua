<p class="TextBodyCopy">Irish law requires the directors to prepare financial statements for each financial period giving a true and fair view of the state of the Society’s affairs at the end of the financial period and of its profit or loss for the financial period.  Under that law the Directors have elected to prepare the financial statements in accordance with Generally Accepted Accounting Practice in Ireland (accounting standards issued by the Financial Reporting Council and promulgated by the Institute of Chartered Accountants in Ireland and Irish law).  </p>
<p class="TextBodyCopy">In preparing these financial statements, the Directors are required to:</p>
