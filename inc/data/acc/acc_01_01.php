<h2 class="TextSubHeading1">Principal Activities</h2>
<p class="TextBodyCopy">The Group is engaged in the purchase, marketing, and sale of consumer dairy products and dairy food ingredients worldwide through its subsidiaries and extensive network of agents and distributors. Consumer dairy products are marketed primarily under the Kerrygold, Pilgrims Choice, Kerrygold Dubliner, MU and Beo brands and the Consumer Foods division’s activities are supported by cheese packing facilities in the UK and butter packing facilities in Germany. The Dairy Trading &amp; Ingredients division sells and trades products across the globe and its facilities include powder blending and packing in the UK and ingredient cheese processing in Spain, the UK and US.</p>
<p class="TextBodyCopy">The Group owns a speciality food distribution network in the US offering refrigerated, frozen and dry food distribution to both local and national food retailers. The network consists of four operating divisions each with their own marketing, sales, warehousing and distribution facilities.</p>
<p class="TextBodyCopy">The Group reported a Turnover of over €2.34 billion in 2014, up 10% year-on-year and a very strong Operating Surplus of €30.3 million, up 17% on prior year. The strong operating performance enabled a €12 million bonus to be declared to members, included in this was a cash bonus of €7.5 million, up 15% on the previous year. These strong results built on the excellent performance in 2013.</p>
<p class="TextBodyCopy">The Group closed the year with a very strong Balance Sheet, with net assets of €436 million. As part of the drive to increase capacity in advance of the removal of quotas, major capital expenditure and working capital investments were made across our key markets. As a result of this the Group ended the year with net debt of €99.3 million but the significant investments made leave the Group well placed for the post-quota environment.</p>
<p class="TextBodyCopy">Access to capital allowing the flexibility and strength to invest in the future of the business is essential to its strategy for growth. In March of 2014, IDB secured a new five year syndicated bank facility of €420 million, replacing its existing three year €350 million syndicated bank facility. These new syndicated five year facilities comprise a financing agreement with facilities available of €165 million and a RID facility with committed facilities of €200 million, stepping up to €255 million over the period of the facility. These new increased facilities were significantly over-subscribed and provide IDB and the Irish dairy industry with significant funds to meet domestic expansion and international growth requirements.</p>
<p class="TextBodyCopy">The Group’s Consumer Foods division reported a solid result for 2014, with all markets contributing well despite the ongoing challenges faced by customers. Continued investments in brand growth and NPD have strengthened the position and offering of Kerrygold and our other brands such as Pilgrims Choice and Kerrygold Dubliner. 2014 was a year which saw some large-scale NPD projects reach fruition and be successfully launched in international markets. </p>
<p class="TextBodyCopy">The Dairy Trading &amp; Ingredients division reported a solid trading performance for 2014, with strong growth in volume, Turnover and Operating Surplus. The Republic of Ireland and US Food Ingredients businesses continued to perform strongly. The UK market had a challenging year; however DTI UK returned a solid performance. The year’s highlights included the opening of a new state-of-the-art facility at the Hilbert site, Wisconsin, the acquisition of a Spanish cheese business and FoodTec in the UK.</p>
<p class="TextBodyCopy">DPI, IDB’s US based speciality food distributor, continued to grow sales in 2014, by both winning new customer contracts and solid year-on-year growth. In 2014 Turnover was up 9.1% with DPI clearly maintaining its place as the third largest US distributor of speciality foods to the retail sector. The review of operations completed by management in 2013 delivered for the business in 2014 with increased profitability reported.</p>
