<h2 class="TextSubHeading1">30. Post retirement liabilities</h2>
<p class="TextBodyCopy">The Group accounts for its post retirement liabilities in accordance with FRS17 - ‘Retirement Benefits’.</p>
<p class="TextBodyCopy">The Parent Society and certain subsidiaries operate and contribute to defined benefit and defined contribution schemes in addition to a number of internally funded arrangements.</p>
<p class="TextBodyCopy">The total profit and loss account charge in respect to defined benefit schemes for the Group was a charge of €1.9m (2013 : €2.2m) of which €2.1m (2013 : €1.8m) has been charged against operating surplus and €0.2m (2013 : €0.4m has been charged) has been credited within other finance costs.</p>
<p class="TextBodyCopy">Contributions to defined contribution pension schemes in the period were €2.5m (2013 : €2.1m).</p>
<p class="TextBodyCopy">The trustees of the Parent Society scheme have obtained an actuarial valuation dated 1 January 2014 using the projected unit valuation method. The trustees of the Adams Foods Limited scheme have obtained a valuation dated 31 December 2012. Valuations as at 27 December 2014 have been obtained for the internally funded schemes. These valuations, and the most recent actuarial valuations of the other post retirement schemes, have been updated by independent qualified actuaries to take account of the requirements of FRS17, in order to assess the liabilities of the schemes as at 27 December 2014.</p>
<p class="TextBodyCopy">It has been agreed that an employer contribution rate of 20.6% of pensionable pay plus an additional €1.0m will apply in future years for the Irish scheme and the expected contributions for the coming financial period are €1.9m. For the other schemes it has been agreed that an employer contribution rate of 19% of pensionable salary plus an additional €0.4m will apply in future years and that the expected contributions for the coming financial period are €0.8m.</p>
