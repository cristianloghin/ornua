<p class="TextBodyCopy">The shareholding of the Parent Society is subdivided into a number of classes of shares.</p>
<p class="TextBodyCopy">The main classes of shares are A, B, C and D shares as well as bonus and deferred shares of €1 each.</p>
<p class="TextBodyCopy">The holders of “A” and “B” shares are entitled to bonus shares and convertible loan stock. They are also entitled to attend and vote at General Meetings of the Society.</p>
<p class="TextBodyCopy">Bonus shares rank pari passu with “A” and “B” shares. Bonus shares issued during the period relate to the redemption of loan stock.</p>
<p class="TextBodyCopy">The holders of “C” and “D” shares are not entitled to bonus shares or convertible loan stock issued nor are they entitled to vote at General Meetings of the Society.</p>
<p class="TextBodyCopy">The holders of deferred shares do not have the right to attend or vote at the General Meetings of the Society.</p>