<p class="TextBodyCopy">In March 2014, the Group entered into a five year syndicated financing agreement with facilities available of €165m.</p>
<p class="TextBodyCopy">All material subsidiaries of the Group entered into cross guarantees for the debts under this agreement and also are subject to a negative pledge that security will not be granted to any party during the course of the agreement.</p>
<p class="TextBodyCopy">The Group is subject to certain financial covenants and other restrictions during the period of this agreement.</p>
<p class="TextBodyCopy">Separately, in March 2014, a number of member suppliers to the Group entered into a five year agreement (reverse invoice discounting or RID facility) with a panel of International Banks led by Rabobank International to sell amounts owed to them by the Group, with committed facilities of €200m, stepping up to €255m over the period of the facility.</p>
<p class="TextBodyCopy">Under this agreement, the Group acknowledges invoices on behalf of the member suppliers to enable them to receive early payment of those invoices. Upon acknowledgement of member invoices which have been sold to Rabobank, the Group’s obligations to the member is extinguished and the Group thereafter assumes an obligation to pay the amount of member invoices sold (2014: €141.8m, 2013: €151.3m) to Rabobank.</p>